void UnlockPinF0()
{
	HWREG(GPIO_PORTF_BASE + GPIO_O_LOCK) = GPIO_LOCK_KEY;
	HWREG(GPIO_PORTF_BASE + GPIO_O_CR) = 0xff;
	HWREG(GPIO_PORTF_BASE + GPIO_O_LOCK) = 0;
}

